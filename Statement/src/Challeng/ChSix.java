package Challeng;

public class ChSix {
    public static void main(String[] args) {
        // Write a program to check whether a number is even or odd


        // case output
        // (6) -> 6 is even number
        // (5) -> 5 is odd number
        // (12) -> 12 is even number
        int n = 5;

        if (n % 2 == 0) {
            System.out.println(n+" is even number ");
        } else {
            System.out.println(n+" is odd number ");
        }


    }
}
