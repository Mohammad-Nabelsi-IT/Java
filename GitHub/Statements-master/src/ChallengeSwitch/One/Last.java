package ChallengeSwitch.One;

public class Last {
    public static void main(String[] args) {

        char grade1 = 'A';
        if (grade1 == 'A') {
            System.out.println("Excellent");
        } else if (grade1 == 'B') {
            System.out.println("Good");
        } else if (grade1 == 'C') {
            System.out.println("Fair");
        } else if (grade1 == 'D') {
            System.out.println("Poor");
        } else {
            System.out.println("Invalid Choice");
        }
    }
}
